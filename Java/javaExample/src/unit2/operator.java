package unit2;
//4
public class operator {

	public static void main(String[] args) {
		int a = 7, b;
		b = a;
		System.out.println(b);
		b += a;
		System.out.println(b);
		b -= a;
		System.out.println(b);
		b *= a;
		System.out.println(b);
		b /= (double)a;
		System.out.println(b);
		b &= a;
		System.out.println(b);
		b |= a;
		System.out.println(b);
		b ^= a;
		System.out.println(b);
	}

}
